const langToggle = document.getElementById('lang-toggle');

function updateContent() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            let content = translations[currentLang][key];

            // Reemplazar [[LOGO]] con el SVG inyectando la clase correcta
            if (content.includes('[[LOGO]]')) {
                content = content.replace('[[LOGO]]', LOGO_SVG);
                el.innerHTML = content;
            } else {
                el.textContent = content;
            }
        }
    });

    langToggle.textContent = translations[currentLang].toggle_lang;
    document.documentElement.lang = currentLang;
}

