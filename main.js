// --- IMAGE PRELOADING (OPCIÓN A) ---
function preloadTourImages() {
    Object.values(tourImages).forEach(src => {
        const img = new Image();
        img.src = src;
    });
    console.log("Novum: Preloading tour images completed.");
}

document.querySelectorAll('.tour-list-item').forEach(item => {
    item.addEventListener('click', () => {
        const key = item.getAttribute('data-tour');

        // Update Active Class
        document.querySelector('.tour-list-item.active').classList.remove('active');
        item.classList.add('active');

        // Update Image
        const mockupImg = document.getElementById('tour-mockup-img');
        mockupImg.style.opacity = '0';

        setTimeout(() => {
            mockupImg.src = tourImages[key];
            mockupImg.style.opacity = '1';

            // Update Title & Desc
            document.getElementById('tour-feat-title').setAttribute('data-i18n', `feat_${key}`);
            document.getElementById('tour-feat-desc').setAttribute('data-i18n', `feat_${key}_desc`);
            updateContent();
        }, 200);
    });
});

document.querySelectorAll('.category-tab').forEach(tab => {
    tab.addEventListener('click', () => {
        const cat = tab.getAttribute('data-category');

        // Update Active Tab
        document.querySelector('.category-tab.active').classList.remove('active');
        tab.classList.add('active');

        // Show/Hide items
        document.querySelectorAll('.tour-list-item').forEach(item => {
            if (item.getAttribute('data-cat') === cat) {
                item.style.display = 'block';
            } else {
                item.style.display = 'none';
            }
        });

        // Click first item of category
        document.querySelector(`.tour-list-item[data-cat="${cat}"]`).click();
    });
});

// Load saved language
window.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('novum-lang');
    if (savedLang && translations[savedLang]) {
        currentLang = savedLang;
    }
    updateContent(); // Initialize content with current language (saved or default)
    preloadTourImages(); // Preload images for instant switching

    // Boot Product Tour
    const activeTab = document.querySelector('.category-tab.active');
    if (activeTab) activeTab.click();
});

// Simple Navbar Scroll Effect
window.addEventListener('scroll', () => {
    const nav = document.querySelector('nav');
    if (window.scrollY > 50) {
        nav.style.padding = '10px 5%';
        nav.style.boxShadow = '0 5px 20px rgba(0,0,0,0.1)';
    } else {
        nav.style.padding = '20px 5%';
        nav.style.boxShadow = 'none';
    }
});
